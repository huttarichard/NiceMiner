
# speed units 
def Sols(value):
    return value / 1000000000

def MSols(value):
    return value / 1000000

def kHs(value):
    return value / 1000000

def MHs(value):
    return value / 1000

def GHs(value):
    return value

def THs(value):
    return value * 1000

def PHs(value):
    return value * 1000000
